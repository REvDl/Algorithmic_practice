from collections import defaultdict
from typing import List
from collections import deque


class Solution:
	def minJumps(self, nums: List[int]) -> int:
		n = len(nums)
		prime_idx = defaultdict(list)
		def min_prime_divide(nums: List[int]):
			max_val = max(nums)
			spf = list(range(max_val + 1))
			for i in range(2, int(max_val ** 0.5) + 1):
				if spf[i] == i:
					for j in range(i * i, max_val + 1, i):
						if spf[j] == j:
							spf[j] = i
			return spf
		spf = min_prime_divide(nums)
		for idx, num in enumerate(nums):
			temp = num
			while temp > 1:
				p = spf[temp]
				prime_idx[p].append(idx)
				while temp % p == 0:
					temp //= p
		queue = deque([0])
		visited = set()
		steps = 0
		while queue:
			for _ in range(len(queue)):
				curr_idx = queue.popleft()
				if curr_idx == n - 1:
					return steps
				for next_idx in (curr_idx - 1, curr_idx + 1):
					if 0 <= next_idx < n and next_idx not in visited:
						visited.add(next_idx)
						queue.append(next_idx)
				val = nums[curr_idx]
				if spf[val] == val:
					for j in prime_idx[val]:
						if j not in visited:
							visited.add(j)
							queue.append(j)
					prime_idx[val] = []
			steps += 1
		return steps







